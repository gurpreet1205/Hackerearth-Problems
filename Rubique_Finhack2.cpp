#include<bits/stdc++.h>
using namespace std;
long long int n,m,c;
long long int a[1001][1001];
long long int chess(long long int x,long long int y)
{
	if(a[x][y]==1)
		return 0;
	else
	{
		a[x][y]=1;
		c++;
		return 1;
	}
}
int main()
{
	long long int t,n,m,i,j,x,y,p,q,r,s,d,flag;
	cin>>t;
	while(t--)
	{
		s=0;
		d=1;
		c=0;
		cin>>n>>m>>x>>y;
		long long int g[n+1],h[m+1],o[1000000],l,k,u;
		for(i=0;i<n;i++)
		{
			for(j=0;j<m;j++)
			{
				a[i][j]=0;
			}
		}
		a[x][y]=1;
		g[0]=x;
		h[0]=y;
		l=1;
		k=0;
		c=1;
		o[0]=1;
		u=0;
		while(1)
		{
			if(k==o[u])
			{
				d++;
				u++;
				o[u]=l;
			}
			flag=0;
			p=g[k]-2;
			q=h[k]-1;
			while(p<0)
				p=n+p;
			while(q<0)
				q=m+q;
			r=chess(p,q);
			if(r==1)
			{
				flag=1;
				g[l]=p;
				h[l]=q;
				l++;
			}
			s=s+r;
			p=g[k]+2;
			q=h[k]-1;
			while(p>=n)
				p=p-n;
			while(q<0)
				q=m+q;
			r=chess(p,q);
			if(r==1)
			{
				flag=1;
				g[l]=p;
				h[l]=q;
				l++;
			}
			s=s+r;
			p=g[k]-2;
			q=h[k]+1;
			while(p<0)
				p=n+p;
			while(q>=m)
				q=q-m;
			r=chess(p,q);
			if(r==1)
			{
				flag=1;
				g[l]=p;
				h[l]=q;
				l++;
			}
			s=s+r;
			p=g[k]+2;
			q=h[k]+1;
			while(p>=n)
				p=p-n;
			while(q>=m)
				q=q-m;
			r=chess(p,q);
			if(r==1)
			{
				flag=1;
				g[l]=p;
				h[l]=q;
				l++;
			}
			s=s+r;
			p=g[k]-1;
			q=h[k]-2;
			while(p<0)
				p=n+p;
			while(q<0)
				q=m+q;
			r=chess(p,q);
			if(r==1)
			{
				flag=1;
				g[l]=p;
				h[l]=q;
				l++;
			}
			s=s+r;
			p=g[k]+1;
			q=h[k]-2;
			while(p>=n)
				p=p-n;
			while(q<0)
				q=m+q;
			r=chess(p,q);
			if(r==1)
			{
				flag=1;
				g[l]=p;
				h[l]=q;
				l++;
			}
			s=s+r;
			p=g[k]-1;
			q=h[k]+2;
			while(p<0)
				p=n+p;
			while(q>=m)
				q=q-m;
			r=chess(p,q);
			if(r==1)
			{
				flag=1;
				g[l]=p;
				h[l]=q;
				l++;
			}
			s=s+r;
			p=g[k]+1;
			q=h[k]+2;
			while(p>=n)
				p=p-n;
			while(q>=m)
				q=q-m;
			r=chess(p,q);
			if(r==1)
			{
				flag=1;
				g[l]=p;
				h[l]=q;
				l++;
			}
			s=s+r;
			if(flag==0||c==n*m)
				break;
			k++;
		}
		cout<<c<<" "<<flag<<"\n";
		if(c!=n*m)
			cout<<"-1\n";
		else
			cout<<d<<"\n";
	}
	return 0;
}